<p><b>Current project status.</b> The playable prototype has been tested in action for countless times by me and others, as I was constantly playing it on the same machine with buddies, or even with fans and testers from all around the world, by having me share my desktop using TeamViewer and taking turns with the cursor; also other fans did it on their own and constantly reported glitches and other issues. When something was bad, there was a flood of comments, emails and new github issues all addressing the same problems. This resulted in a lot of gameplay improvements and unit balancing, therefore most unit abilities got heavily tweaked and even totally revamped, sometimes even more than once, as many times as it takes to get the right feel required. We don't have deadlines and we're not forced to sit in some office while eagerly awaiting for the time to pass faster so that we can run home, eat sleep and repeat. Hell no, we do this out of passion and that sometimes means being stuck in limbo for quite a while until getting things straight and then we move on. The prototype was coded from scratch by one of the 3d artists volunteering on the project while building a portfolio, since he was a Heroes of Might and Magic 3 fan and also liked our concept art. He could have made fanart of characters from well known franchises like most artists do, but instead he decided helping out a project that was (and still is) in dire need.</p>

