<p>
This mode has persistent sessions lasting one month each, having a limited time at the start of the month when players can sign up for it. There are a limited number of free slots and if those are all taken, then a paid subscription is required for that specific month. This is one of the methods that allows fans to support the game's mentenance and development, because we all want things to run smooth and look good, don't we? The plasma pool won't be replenished between battles, the same goes for units, which die for good. Players will need to gather the necessary resources in order to build their city, having structures that will generate new units and replenish the plasma pool in order to be able to defend themselves or take offensive actions against enemy players. If your Dark Priest gets killed during battle, you won't be able to play another match in this mode until he revives, but you can spend plasma points to instantly revive him. When entering a match, you'll be required to have a minimum number of non-duplicated units and at least a certain number of plasma points. The score of each match will be constantly accumulted in order to reflect your position in the expansion's ladder. The players with the highest score each month will be immortalized in the hall of fame and also receive awesome prizes. You can play this mode only online, optionally by teaming up with another buddy, who also requires an account or subscription. Your buddy can even play from the same device as you provided authorization is granted or he uses a mobile device as a controller.
</p>

